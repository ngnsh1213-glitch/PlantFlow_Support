  m1.uniteWith(pl1)
  pl1.erase()

  if (Dn <= 600):
    pad_size = PIPE_PAD(Dn, Qn)
    pC = pad_size["C"]
    pL = pad_size["L"]
    pR = Dz/2 + PW

  else:
    pC = Qz * 2
    pL = Qz * 1.5
    pR = Dz/2 + PW

  pp1 = CYLINDER(s, R = pR, H = pC, O = 0.0)
  o11 = CYLINDER(s, R1 = 4 * pC/2, R2 = 4 * pL/2, H = pC * 2, O = 0.0)
  o12 = CYLINDER(s, R1 = pC/2, R2 = pR/1.5, H = pC * 2, O = 0.0)
  o11.subtractFrom(o12)
  o12.erase()
  o11.rotateY(90).translate((-pC, pR, pC/2))
  pp1.subtractFrom(o11)
  o11.erase()

  pp1.rotateZ(90).translate((A, 0.0, -pC/2))
  m1.uniteWith(pp1)
  pp1.erase()

  o13 = CYLINDER(s, R = Dz/2, H = pC, O = 0.0)
  o13.translate((A, 0.0, -pC/2))
  m1.subtractFrom(o13)
  o13.erase()

