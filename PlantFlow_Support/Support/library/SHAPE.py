def TRANS_BOX(s, L = 100.0, W = 100.0, H = 100.0, BASE = "BOTTOM_05", 
  ID = "TRANS_BOX", **kwargs):
  '''
  '''
  b1 = BOX(s, L = L, W = W, H = H)
  if (BASE == "BOTTOM_01"):
    b1.translate((H/2, L/2, W/2))
  elif (BASE == "BOTTOM_02"):
    b1.translate((0.0, L/2, W/2))
  elif (BASE == "BOTTOM_03"):
    b1.translate((-H/2, L/2, W/2))
  elif (BASE == "BOTTOM_04"):
    b1.translate((-H/2, 0.0, W/2))
  elif (BASE == "BOTTOM_05"):
    b1.translate((0.0, 0.0, W/2))
  elif (BASE == "BOTTOM_06"):
    b1.translate((H/2, 0.0, W/2))
  elif (BASE == "BOTTOM_07"):
    pass
  elif (BASE == "BOTTOM_08"):
    b1.translate((0.0, -L/2, W/2))
  elif (BASE == "TOP_04"):
    b1.translate((-H/2, 0.0, -W/2))

activate(TRANS_BOX)

