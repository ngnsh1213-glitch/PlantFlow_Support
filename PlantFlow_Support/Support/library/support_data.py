_SPAN_CASE_INDEX = {"EMPTY": 0, "FULL_WATER": 1, "EMPTY_INS": 2, "FULLWATER_INS": 3}
_span_table_cache = None


def _span_table_path():
    return _os.path.join(_os.path.dirname(_os.path.abspath(__file__)), "span_table_JIS.json")


def load_span_table():
    """span_table_JIS.json을 1회 로드해 캐시한다. 파일 부재/파싱 실패 시 명시적 예외(빈 dict 반환 금지)."""
    global _span_table_cache
    if _span_table_cache is not None:
        return _span_table_cache
    path = _span_table_path()
    try:
        with open(path, "r", encoding="utf-8") as f:
            data = _json.load(f)
    except (IOError, OSError) as e:
        raise RuntimeError("span_table_JIS.json 로드 실패: {0} ({1})".format(path, e))
    except ValueError as e:
        raise RuntimeError("span_table_JIS.json 파싱 실패: {0} ({1})".format(path, e))
    if "max_span" not in data:
        raise RuntimeError("span_table_JIS.json 구조 오류: 'max_span' 키 없음")
    _span_table_cache = data
    return data


def max_span_JIS(dn, case="FULL_WATER"):
    """JIS 호칭(정수) + 하중케이스 → 최대 지지간격(mm). 허용키/케이스 외 입력은 KeyError로 fail-closed."""
    data = load_span_table()
    table = data["max_span"]
    key = str(int(dn))
    if key not in table:
        raise KeyError("스팬표 허용키 외 호칭: {0}A (허용=20~600A, 보간 금지)".format(key))
    if case not in _SPAN_CASE_INDEX:
        raise KeyError("알 수 없는 하중케이스: {0} (허용={1})".format(case, list(_SPAN_CASE_INDEX)))
    return table[key][_SPAN_CASE_INDEX[case]]


